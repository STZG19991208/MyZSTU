package cn.edu.newedu;

public class Score {
	private String Xm;
	private String Kcmc;
	private String Bj;
	private String Cj;
	private String Jd;
	private String Jgmc;
	private String Kch;
	private String Kcxzmc;
	private String Ksxz;
	public String toString() {
		return Xm+Kcmc+Bj+Cj+Jd+Jgmc+Kch+Kcxzmc+Ksxz;
	}
	public String getXm() {
		return Xm;
	}
	public void setXm(String xm) {
		Xm = xm;
	}
	public String getKcmc() {
		return Kcmc;
	}
	public void setKcmc(String kcmc) {
		Kcmc = kcmc;
	}
	public String getBj() {
		return Bj;
	}
	public void setBj(String bj) {
		Bj = bj;
	}
	public String getCj() {
		return Cj;
	}
	public void setCj(String cj) {
		Cj = cj;
	}
	public String getJd() {
		return Jd;
	}
	public void setJd(String jd) {
		Jd = jd;
	}
	public String getJgmc() {
		return Jgmc;
	}
	public void setJgmc(String jgmc) {
		Jgmc = jgmc;
	}
	public String getKch() {
		return Kch;
	}
	public void setKch(String kch) {
		Kch = kch;
	}
	public String getKcxzmc() {
		return Kcxzmc;
	}
	public void setKcxzmc(String kcxzmc) {
		Kcxzmc = kcxzmc;
	}
	public String getKsxz() {
		return Ksxz;
	}
	public void setKsxz(String ksxz) {
		Ksxz = ksxz;
	}
}
